package com.hipolito.app.model;

public class Curso {
	
	private  int idUniverdad;
	private String curso;
	private String descrripcion;
	
	
	public int getIdUniverdad() {
		return idUniverdad;
	}
	public void setIdUniverdad(int idUniverdad) {
		this.idUniverdad = idUniverdad;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getDescrripcion() {
		return descrripcion;
	}
	public void setDescrripcion(String descrripcion) {
		this.descrripcion = descrripcion;
	}

}
